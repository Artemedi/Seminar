#   Найти произведение пар чисел в списке. Парой считаем первый и последний элемент, второй и предпоследний и т.д. 
#   Пример: [2, 3, 4, 5, 6] => [12, 15, 16]; [2, 3, 5, 6] => [12, 15]

lst = [2, 3, 4, 5, 6]
for i in range(0,len(lst)):
    if (len(lst)//2 == 0):
        sum = lst[i] * lst[len(lst) -i -1]
    else:
        sum = lst[i] * lst[len(lst) -i -1]   
    print(sum, end = ' ')